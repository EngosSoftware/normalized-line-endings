//! # Common definitions.

/// NULL U+0000
pub const NUL: char = '\u{0000}';

/// START OF HEADING U+0001
pub const SOH: char = '\u{0001}';

/// START OF TEXT U+0002
pub const STX: char = '\u{0002}';

/// END OF TEXT U+0003
pub const ETX: char = '\u{0003}';

/// END OF TRANSMISSION U+0004
pub const EOT: char = '\u{0004}';

/// ENQUIRY U+0005
pub const ENQ: char = '\u{0005}';

/// ACKNOWLEDGE U+0006
pub const ACK: char = '\u{0006}';

/// BELL U+0007
pub const BEL: char = '\u{0007}';

/// BACKSPACE U+0008
pub const BS: char = '\u{0008}';

/// CHARACTER TABULATION U+0009
pub const TAB: char = '\u{0009}';

/// LINE FEED U+000A
pub const LF: char = '\u{000A}';

/// LINE TABULATION U+000B
pub const VT: char = '\u{000B}';

/// FORM FEED U+000C
pub const FF: char = '\u{000C}';

/// CARRIAGE RETURN U+000D
pub const CR: char = '\u{000D}';

/// SHIFT OUT U+000E
pub const SO: char = '\u{000E}';

/// SHIFT IN U+000F
pub const SI: char = '\u{000F}';

/// DATA LINK ESCAPE U+0010
pub const DLE: char = '\u{0010}';

/// DEVICE CONTROL ONE U+0011
pub const DC1: char = '\u{0011}';

/// DEVICE CONTROL TWO U+0012
pub const DC2: char = '\u{0012}';

/// DEVICE CONTROL THREE U+0013
pub const DC3: char = '\u{0013}';

/// DEVICE CONTROL FOUR U+0014
pub const DC4: char = '\u{0014}';

/// NEGATIVE ACKNOWLEDGE U+0015
pub const NAK: char = '\u{0015}';

/// SYNCHRONOUS IDLE U+0016
pub const SYN: char = '\u{0016}';

/// END OF TRANSMISSION BLOCK U+0017
pub const ETB: char = '\u{0017}';

/// CANCEL U+0018
pub const CAN: char = '\u{0018}';

/// END OF MEDIUM U+0019
pub const EM: char = '\u{0019}';

/// SUBSTITUTE U+001A
pub const SUB: char = '\u{001A}';

/// ESCAPE U+001B
pub const ESC: char = '\u{001B}';

/// FILE SEPARATOR U+001C
pub const FS: char = '\u{001C}';

/// GROUP SEPARATOR U+001D
pub const GS: char = '\u{001D}';

/// RECORD SEPARATOR U+00E
pub const RS: char = '\u{001E}';

/// UNIT SEPARATOR U+001F
pub const US: char = '\u{001F}';
